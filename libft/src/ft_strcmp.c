int		ft_strcmp(const char *s1, const char *s2)
{
	int		i;
	int		c1;
	int		c2;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
		{
			c1 = s1[i];
			c2 = s2[i];
			if (s1[i] < 0)
				c1 *= -1;
			if (s2[i] < 0)
				c2 *= -1;
			return ((int)(c1 - c2));
		}
		i++;
	}
	return (0);
}
